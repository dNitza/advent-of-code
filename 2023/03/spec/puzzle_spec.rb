require_relative "../lib/puzzle"

RSpec.describe Puzzle do
  
  subject { described_class.new }

  it "part 1" do
    input = <<-INPUT
467..114..
...*......
..35..633.
......#...
617*......
.....+.58.
..592.....
......755.
...$./\....
.664.598..
....*....
678..210.
    INPUT

    expect(subject.part1(input.split("\n").map(&:chomp))).to eq 4571
  end

  it "part 2" do
    input = <<-INPUT
11.467....
*..*......
....35....
......#...
617*......
.....+.58.
..592.....
......755.
...$.*....
.664.598..
    INPUT

    expect(subject.part2(input.split("\n").map(&:chomp))).to eq 467835
  end
end
